public class Student {
    int rollNo;
    String name, email;

    //getter
    int getRollNo(){
        return rollNo;
    }
    String getName(){
        return name;
    }
    String getEmail(){
        return email;
    }
    //setter
    void setRollNo(int rollNo){
        this.rollNo=rollNo;
    }
    void setName(String name){
        this.name=name;
    }
    void setEmail(String email){
        this.email=email;
    }
}
